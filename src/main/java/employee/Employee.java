package main.java.employee;

public class Employee {
	    private int ID;
	    private String name;
	    private int salary;
	    
	    
		public Employee() {
			super();
			this.name = name;
			this.salary = salary;
		}
		
		public int getID() {
			return ID;
		}
		public void setID(int iD) {
			ID = iD;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public int getSalary() {
			return salary;
		}
		public void setSalary(int salary) {
			this.salary = salary;
		}

}
